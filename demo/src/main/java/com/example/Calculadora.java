package com.example;

public class Calculadora {
    
    public Integer soma(Integer a, Integer b){
        return a + b;
    }

    
    public Integer subtracao(Integer a, Integer b){
        return a - b;
    }

    
    public Integer multiplicacao(Integer a, Integer b){
        return a * b;
    }

    
    public Integer divisao(Integer a, Integer b){
        
        return a / b;
    }
}
